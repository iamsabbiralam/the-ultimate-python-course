a = (1, 2, 3, 45, 5, 45)
print(a)

no = a.count(45) # count() method returns the number of times a specified value occurs in a tuple
print(no)

i = a.index(45) # index() method finds the first occurrence of the specified value
print(i)
print(len(a)) # len() method returns the number of items in a tuple